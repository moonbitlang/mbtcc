

#include <stdio.h>

void print_char(char x) {
  printf("%c", x);
}

void println_char(char x) {
  printf("%c\n", x);
}

void print_int(int x) {
  printf("%d", x);
}

void println_int(int x) {
  printf("%d\n", x);
}

void print_uint(unsigned int x) {
  printf("%u", x);
}

void println_uint(unsigned int x) {
  printf("%u\n", x);
}

void print_long(long x) {
  printf("%ld", x);
}

void println_long(long x) {
  printf("%ld\n", x);
}

void print_float(float x) {
  printf("%f", x);
}

void println_float(float x) {
  printf("%f\n", x);
}

void print_double(double x) {
  printf("%lf", x);
}

void println_double(double x) {
  printf("%lf\n", x);
}

